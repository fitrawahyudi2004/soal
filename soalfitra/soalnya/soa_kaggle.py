

def operasi_aritmatika(num1, num2):
    # Penjumlahan
    hasil_penjumlahan = num1 + num2

    # Pengurangan
    hasil_pengurangan = num1 - num2

    # Perkalian
    hasil_perkalian = num1 * num2

    # Pembagian
    hasil_pembagian = num1 / num2

    return hasil_penjumlahan, hasil_pengurangan, hasil_perkalian, hasil_pembagian
